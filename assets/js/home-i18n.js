document.addEventListener("alpine:init", function () {
  Alpine.data("homeNavbar", function (lang) {
    return {
      open: false,
      lang: lang,
      isDark: document.documentElement.classList.contains("dark"),

      init: function () {
        var availableLanguages = ["en", "es", "fr", "it"];

        // Sync lang with the actual URL segment so the dropdown reflects current language
        var urlParts = window.location.pathname.split("/");
        var urlLang = urlParts.find(function (s) {
          return s.length === 2 && availableLanguages.includes(s);
        });
        if (urlLang) {
          this.lang = urlLang;
        }

        var STORAGE_KEY = "lang_redirect_done";

        if (sessionStorage.getItem(STORAGE_KEY)) {
          return;
        }

        var path = window.location.pathname;
        var segments = path.replace(/^\/+/, "").split("/");
        var firstSegment = segments[0];

        var browserLangs = navigator.languages || [
          navigator.language || navigator.userLanguage,
        ];

        var selected = "en";
        for (var i = 0; i < browserLangs.length; i++) {
          var short = browserLangs[i].toLowerCase().split("-")[0];
          if (availableLanguages.includes(short)) {
            selected = short;
            break;
          }
        }

        if (availableLanguages.includes(firstSegment)) {
          segments[0] = selected;
        } else {
          segments.unshift(selected);
        }

        var newPath = "/" + segments.join("/");
        var newUrl = newPath + window.location.search + window.location.hash;

        sessionStorage.setItem(STORAGE_KEY, "1");

        if (
          newUrl !==
          window.location.pathname +
            window.location.search +
            window.location.hash
        ) {
          window.location.replace(newUrl);
        }
      },

      toggleTheme: function () {
        this.isDark = !this.isDark;
        var value = this.isDark ? "dark" : "light";
        localStorage.setItem("theme", value);
        document.documentElement.classList.toggle("dark", value === "dark");
      },
      toggle: function () {
        this.open = !this.open;
      },
      close: function () {
        this.open = false;
      },
      isActive: function (code) {
        return this.lang === code;
      },
      langUrl: function (code) {
        var parts = window.location.pathname.split("/");
        parts[1] = code;
        return parts.join("/");
      },
    };
  });
});

(function () {
  var availableLanguages = ["en", "es", "fr", "it"];
  var parts = window.location.pathname.split("/");
  var segment = parts.find(function (s) {
    return s.length === 2 && availableLanguages.includes(s);
  });
  var lang = segment && translations[segment] ? segment : "en";
  var t = translations[lang];

  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    var key = el.dataset.i18n;
    if (t[key] === undefined) return;
    if (el.tagName === "META") el.setAttribute("content", t[key]);
    else if (el.tagName === "TITLE") el.textContent = t[key];
    else el.textContent = t[key];
  });

  if (lang !== "en") {
    document.querySelectorAll("[data-lang-href]").forEach(function (el) {
      var path = el.dataset.langHref;
      el.setAttribute("href", "/" + lang + "/" + path);
    });
  }

  document.documentElement.setAttribute("lang", lang);
})();
